package vindinium.game;

/**
 * Simple interface for a 2D position on a grid
 * 
 * @author Nick Ferraro
 *
 */
public interface IPosition {
	/**
	 * Get the x value of this position
	 * 
	 * @return The x value
	 */
	public int getX();
	
	/**
	 * Get the y value of this position
	 * 
	 * @return The y value
	 */
	public int getY();
}
